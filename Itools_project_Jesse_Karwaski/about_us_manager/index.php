    <h1 style="text-align: center">About us</h1>
    <h2>History of the NFC</h2>

    <p>
    The Northern Football Conference (NFC) was formed in 1954 as the Northern Ontario Rugby Football Union (NORFU). Its purpose, 
    then as it is now, is to provide an opportunity for athletes over the age of 19 to play football beyond the high school level. 
    Over 6700 players have availed themselves of the opportunity to play in the over 900 scheduled NFC games. 
    Many players have gone on to star in University/College and Professional football. Likewise, many star University/College players have chosen to continue playing
     competitive football in the NFC. NFC football is played at a very high caliber and is very entertaining and exciting brand of play.
    </p>
    <br>
    <p>
    The newly formed league began playing in 1954 with four teams participating: Kirkland Lake Alouettes, North Bay Roughriders, Sudbury Hardrocks and 
    Tri-Town (Cobalt, Haileybury, New Liskeard) Raiders, and has operated annually with as many as eleven teams and as few as three teams since that time.

    </p>
<br>
    <p>
In the intervening years other teams, communities and clubs have entered and departed the league. 
In total there have been thirty teams representing 19 communities and 1 team representing Laurentian University. 
The former membership includes: Sturgeon Falls Bombers, Rouyn-Noranda Fantassins, North Renfrew Rams, North Bay Ti-Cats, 
Val D'Or Jets, Timmins Falcons, Kirkland Lake Kougars, Sault Ste. Marie Steelers, 
Laurentian University Voyageurs, Bramalea Satellites, Orillia Silver Bombers, Etobicoke Argonauts, Stoney Creek Patriots, 
North Bay Northmen, Hamilton Wildcats, Brampton Bears, Brampton Bulldogs, Scarborough Crimson Tide, Hamilton Wild Cats, Toronto Eagles, Peterborough Packers, 
Mississauga Wolverines, Oshawa Hawkeyes, Belleville Panthers, Milton Marauders and Durham Hawkeyes.

    </p>
<br>
    <p>
    The current league is composed of ten teams centered in the GTA (GTA All Stars), Toronto (Phantom Raiders), Oakville MIFA Longhorns, Hamilton Steel City Patriots, 
    Ottawa Sooners, North Bay Bulldogs, Sault Ste. Marie Steelers, Sudbury Spartans, Kitchener, Waterloo and Cambridge Tri-City Outlaws and Sarnia Imperials.

    </p>
<br>
    <p>
        The league continues to have a Northern Ontario flavor as befits it name, but it is now a province-wide senior amateur football group that has a very storied history. 
        The league is open to Canadian amateur players who wish to play football beyond the varsity level. 
        The league features no age or weight restrictions and has welcomed new players from across Canada without restrictions to develop and 
        advance their goals as football players.
    </p>
